package Basics;
public class FourPillars {
    
}

public static void main (String[] args){


    // Four Pillars of OOPs ->

    /*
     * 1. Abstraction
     * 2. Inheritance
     * 3. Encapsulation
     * 4. Polymorphism
     */

     /*
      * 
      Abstraction - Providing necessary detail only  

      Abstraction is a technique of providing only the essential details
      to the user by hiding the unnecessary or irrelevent details of an entity.

      Abstraction enables us to provide a simple interface to a user without 
      asking for complex details to perform an action.

      In simpler words, giving the user the ability to drive the car without
      requiring to understand tiny details of 'how does the engine work'.

      Advantages ->
                this helps in reducing the operational complexity at the user-end.

      */

      /*
       * Inheritance -> 
       * 
       * Inheritance is a technique of acquiring the properties of another class having 
       * features in common.
       * 
       * It is also known as a child - parent relationship, where a child inherits
       *  the properties of its parent.
       * 
       * This is the reason it is called "is-a relationship" where the child is-a type of parent
       * 
       * Advantages ->
       *             It allows us to increase the reusability and reduce the 
       * duplication of code.
       * 
       * 
       */

       /*
        * 
        Encapsulation ->
               
        Encapsulation is a technique of restricting a user from directly modifying the data 
        members or variables of a class in order to maintain the integrity and security of the data. 

        How do we do that? We restrict the access of the variable by switching the access-modifier to
         private and exposing public methods that we can use to access the data.

         Advantages ->
                      This makes sure that the intigrity of data is not compromised.



        */

        /*
          
          Polymorphism ->
          
          It is a feature that allows you to perform an action in multiple or different ways.
          
          There are two types of polymorphism :
          
          -> Method Overloading[Compile time] -- Static polymorphism (Static Binding)
          
          -> Method Overriding[Run time] -- Dynamic polymorphism (Dynamic Binding)

          Advantages ->
                Programmers code can be reused via polymorphism.

         */

}
